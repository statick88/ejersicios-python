# Diccionarios
diccionario = {
    "nombre": "Juan",
    "edad": 30,
    "ciudad": "Madrid",
    "ocupacion": "Desarrollador",
    "email": "juan@example.com"
}
print(diccionario)
diccionario["nombre"] = "Carlos" # Modificamos el valor asociado a la clave 'nombre'
print(diccionario) # El diccionario original se ha modificado y ahora contiene el nuevo valor
print(diccionario['edad']) # Accedemos al valor asociado a la clave 'edad'